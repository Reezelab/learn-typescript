// tipe data di typescript mirip sekali dengan tipe data di javascript diantaranya yaitu...


// ---tipe data primitif (tipe data yang sederhana) ---

// string
console.log(typeof 'Hello World!');

// number
console.log(typeof 100);
console.log(typeof 3.14);

// boolean
console.log(typeof true);
console.log(typeof false);

// null, NaN, undefine
console.log(typeof null)
console.log(typeof NaN)
console.log(typeof undefined)



// tipe data non-primitif (tipe data yang kompleks)

// array
console.log(typeof [10, 20, 30, 40, 50]);

// object
console.log(typeof {nama: 'John Doe', address: 'United States'})

// function, enum, dan interface termasuk tipe data non-primitif